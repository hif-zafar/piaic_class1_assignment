# Store the person's name with whitespace characters
person_name_with_whitespace = "\t\n   Hifza Zafar   \t\n"

# Print the name with whitespace
print("Name with whitespace:", person_name_with_whitespace)

# Strip the whitespace from the name
stripped_name = person_name_with_whitespace.strip()

# Print the name after stripping whitespace
print("Stripped Name:", stripped_name)