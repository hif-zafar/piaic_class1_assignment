# Array of favorite pizza names
favorite_pizzas = ["Margherita", "Pepperoni", "Vegetarian", "Fajita"]

# Print the name of each pizza using a for loop
print("Pizza Names:")
for pizza in favorite_pizzas:
    print(pizza)

# Print a sentence for each pizza
print("\nPizza Statements:")
for pizza in favorite_pizzas:
    print(f"I like {pizza} pizza.")

# Additional sentence outside the for loop
print("\nI really love pizza!")
